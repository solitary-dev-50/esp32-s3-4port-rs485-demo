#ifndef CONFIG_H
#define CONFIG_H

#include <Arduino.h>

// ======================================================================================
// JISHI RS485 Controller - 配置文件
// 硬件: JS-V1.5, 4路 RS485 (CH1/CH2 ESP32直连, CH3/CH4 经SC16IS752 SPI扩展)
// ======================================================================================

// ===================================================================
// RS485 通道 1 引脚 (ESP32-S3 直连 UART)
// ===================================================================
#define PIN_RS485_CH1_TX      17
#define PIN_RS485_CH1_RX      18
#define PIN_RS485_CH1_DE_RE   4

// ===================================================================
// RS485 通道 2 引脚 (ESP32-S3 直连 UART)
// ===================================================================
#define PIN_RS485_CH2_TX      13
#define PIN_RS485_CH2_RX      14
#define PIN_RS485_CH2_DE_RE   9
// ===================================================================
// 向后兼容别名 (旧代码仍可使用)
// ===================================================================
#define PIN_RS485_TX    PIN_RS485_CH1_TX
#define PIN_RS485_RX    PIN_RS485_CH1_RX
#define PIN_RS485_DE_RE PIN_RS485_CH1_DE_RE


// ===================================================================
// SC16IS752 SPI 引脚 (用于 RS485 CH3 / CH4)
// ===================================================================
#define PIN_SC16_CS           21
#define PIN_SC16_SCK          42
#define PIN_SC16_MOSI         40
#define PIN_SC16_MISO         41
#define PIN_SC16_IRQ          38

// ===================================================================
// 板载外设引脚 (演示固件暂未全部使用，保留给后续开发)
// ===================================================================
#define PIN_BUZZER            16    // 蜂鸣器 (高电平响)
#define PIN_LED_STRIP_DATA    12    // RGB / WS2812 数据线
#define PIN_I2C_SDA           1     // I2C 扩展
#define PIN_I2C_SCL           2     // I2C 扩展

// ===================================================================
// RS485 通信配置
// ===================================================================
#define RS485_BAUDRATE 38400
#define RESPONSE_TIMEOUT_MS 1000

// ===================================================================
// RS485 通道枚举
// ===================================================================
enum RS485Channel {
    CHANNEL_1 = 0,
    CHANNEL_2 = 1,
    CHANNEL_3 = 2,
    CHANNEL_4 = 3,
    CHANNEL_COUNT = 4
};

// ===================================================================
// WiFi 配置
// ===================================================================
#define WIFI_SSID ""
#define WIFI_PASSWORD ""

// ===================================================================
// Web 服务器配置
// ===================================================================
#define WEB_SERVER_PORT 80

// ===================================================================
// 系统配置
// ===================================================================
#define RELAY_COUNT 16
#define SENSOR_POLLING_INTERVAL_MS 2000

// ===================================================================
// 设备类型定义 (Modbus 从机)
// ===================================================================
#define DEVICE_SENSOR 1
#define DEVICE_RELAY 2

// ===================================================================
// Modbus 功能码定义
// ===================================================================
// Modbus function codes are provided by eModbus (ModbusTypeDefs.h)
// Redundant defines removed to avoid include-order conflicts

// ===================================================================
// 设备配置结构体
// ===================================================================
typedef struct {
    uint8_t address;
    const char* name;
    uint8_t type;
    uint16_t register_addr;
    uint16_t register_count;
    RS485Channel channel;
} DeviceConfig_t;

// ===================================================================
// 设备配置数组声明
// ===================================================================
extern DeviceConfig_t g_devices[];
extern const uint8_t g_device_count;

// ===================================================================
// FreeRTOS 任务配置
// ===================================================================
#define MODBUS_TASK_PRIORITY 5
#define SENSOR_TASK_PRIORITY 4
#define RELAY_TASK_PRIORITY 4
#define WEBSERVER_TASK_PRIORITY 3

#define MODBUS_TASK_STACK_SIZE 4096
#define SENSOR_TASK_STACK_SIZE 3072
#define RELAY_TASK_STACK_SIZE 3072
#define WEBSERVER_TASK_STACK_SIZE 8192

// ===================================================================
// 队列配置
// ===================================================================
#define RELAY_COMMAND_QUEUE_SIZE 10
#define SENSOR_DATA_QUEUE_SIZE 5

#endif // CONFIG_H
